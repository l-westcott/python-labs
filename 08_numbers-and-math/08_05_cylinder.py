# Write the necessary code calculate the volume and surface area
# of a cylinder with a radius of 3.14 and a height of 5.
# Print out the result.
radius = 3.14
height = 5
volume = 3.14 * (radius**2) * height
print("The volume of the cylinder = ", volume)
a = 2 * 3.14 * radius * height
b = 2 * 3.14 * (radius**2)
surface_area = a + b
print("The surface area of the cylinder = ", surface_area)
